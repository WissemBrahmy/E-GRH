Conditions de Travail lorsqu’il existe dans l’entreprise ; <br>
-  Communiqué en deux exemplaires, accompagné de l’avis des représentants du Personnel à 
l’Inspecteur du Travail ; <br>
-  Déposé le 2018/03/15  <br>
-  Affiché dans les locaux de l’entreprise sur le panneau réservé à cet effet le : 2018/04/23 <br>

Un exemplaire est remis à chaque salarié, en particulier lors de l’embauche. <br>

