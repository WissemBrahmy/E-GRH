      <b style="size: 13px;  text-align: justify; line-height: 1.5;" text-wrap>
    <h3>Règlement Intérieur</h3>  
    <h4>Le présent Règlement Intérieur a pour objet de définir :</h4>  
    
<p>-  Les principales mesures en matière d’hygiène et de sécurité dans l’entreprise </p>
<p>-  Les règles générales et permanentes relatives à la discipline </p>
<p>-  Les dispositions relatives aux droits de la défense des salariés.</p>
  
		Le présent Règlement s’applique à l’ensemble des salariés et apprentis sans restriction ni réserve.
		<br>
		<b>ARTICLE 1 – DISCIPLINE GENERALE </b><br>
La durée du travail est fixée conformément aux dispositions légales et conventionnelles en vigueur. 
Les salariés sont astreints à l’horaire arrêté par la Direction tel qu’affiché sur les lieux de travail et 
communiqué à l’Inspection du Travail. 
Les salariés doivent se soumettre aux mesures de contrôle des entrées et des sorties mises en 
application par la Direction. 
Le Personnel doit se trouver à son poste de travail à l’heure fixée pour le début du travail et à celle prévue 
pour la fin de celui-ci. 
Toute absence doit, sauf cas de force majeure, faire l’objet d’une autorisation préalable de la Direction. 
L’absence non autorisée constitue une absence irrégulière. 
Toute indisponibilité consécutive à la maladie doit, sauf cas de force majeure, être justifiée auprès de la 
Direction dans les 48 heures qui suivent l’arrêt. 
		<br>
	<b>	IL EST INTERDIT: </b> <br>
-  de quitter son poste de travail sans autorisation de la Direction. Cette interdiction est limitée par les 
dispositions applicables aux délégués du Personnel et salariés protégés et celles concernant le droit de 
quitter son poste en cas de danger grave et imminent signalé à l’employeur ;  <br>
-  d’introduire et de diffuser à l’intérieur de l’entreprise des tracts, des pétitions, de procéder à des 
affichages non autorisés sous réserve de l’exercice du droit syndical ;  <br>
-  d’introduire sans autorisation dans l’entreprise des personnes étrangères au service sous réserve du 
respect du droit syndical ;  <br>
-  de séjourner dans l’entreprise en dehors des heures de travail sous réserve des dispositions 
concernant les délégués du Personnel ;  <br>
-  d’emporter sans autorisation écrite des objets appartenant à l’entreprise ;  <br>
-  de se livrer à des travaux personnels sur les lieux du travail ;  <br>
-  de se faire adresser de la correspondance ou des colis personnels à l’adresse de l’entreprise ainsi que 
des communications téléphoniques privées ; <br>
-  d’utiliser pour son compte personnel les réseaux d’information de l’entreprise (Internet, Minitel) ; <br>
-  d’introduire dans l’entreprise des marchandises destinées à être vendues pour son compte personnel ;  <br>
-  de se livrer à des collectes, souscriptions sous réserve du droit syndical et sauf autorisation de la 
Direction ;  <br>
-  de divulguer tous renseignements ayant trait aux opérations commerciales, confidentielles dont le 
Personnel aurait connaissance dans l’exercice de ses fonctions ;  <br>
-  de garer son véhicule hors des emplacements prévus à cet effet ;  <br>
-  de quitter son travail sans motif ;  <br>
-  de signer des pièces ou des lettres au nom d’entreprise sans y être expressément autorisé ;  <br>
-  de refuser un travail commandé sauf en cas de grève ;  <br>
-  de conserver des fonds appartenant à l’entreprise ;  <br>
-  de frauder dans le domaine du contrôle de la durée du travail ;  <br>
-  toute action et attitude contraires à la réglementation et à la jurisprudence du droit du travail.  <br>
<b>ARTICLE 2 – HYGIENE ET SECURITE </b><br>
Le Personnel est tenu d’observer les mesures d’hygiène et de sécurité ainsi que les prescriptions de la 
Médecine du Travail qui résultent de la réglementation en vigueur et des décisions du Comité d’Hygiène et 
de Sécurité s’il existe dans l’entreprise.  <br>

<b>IL EST NOTAMMENT OBLIGATOIRE : <b><br>
-  de déposer les vêtements et objets personnels dans les vestiaires mis à disposition. La Direction 
décline toute responsabilité en cas de détérioration,perte ou vol des objets déposés dans les 
vestiaires, dans la mesure où les vestiaires sont munis d’un cadenas fourni par l’employeur ; <br>
-  d’utiliser des accessoires et vêtements de sécurité mis à la disposition des salariés par l’entreprise. <br>
<b>IL EST FORMELLEMENT INTERDIT: </b><br>
-  de pénétrer et de séjourner dans l’entreprise en état d’ébriété ; <br>
-  de consommer dans l’entreprise des boissons alcoolisées en dehors de la quantité strictement 
autorisée pour la consommation à l’occasion des repas ; <br>
-  de prendre ses repas sur les lieux de travail ; <br>
-  de fumer en dehors des locaux prévus par l’entreprise ; <br>
-  de détourner le matériel de sécurité de son utilisation normale ou d’en rendre l’accès difficile. <br>
<b>ARTICLE 3 – DISPOSITIONS RELATIVES A CERTAINES CATEGORIES DE PERSONNEL <b>
<b>IL EST NOTAMMENT INTERDIT: </b><br>

-  de transporter à bord des véhicules des personnes étrangères à l’entreprise ; <br>
-  de se servir des véhicules de l’entreprise à d’autres fins que celles prévues par l’entreprise ; <br>
-  d’emprunter un véhicule de l’entreprise sans une autorisation préalable 
-  de laisser le véhicule en état de malpropreté ; <br>
-  de provoquer ou de subir un accident sans prévenir dès le retour à l’entreprise la personne 
responsable ; <br>
-  de rouler avec un camion ayant une anomalie flagrante de fonctionnement sans le signaler aux 
personnes responsables et risquer, par cela, de provoquer une détérioration plus importante du 
matériel ; <br>
-  de ne pas respecter scrupuleusement les directives de l’entreprise en matière d’encaissement, 
facturation, consignation des emballages. <br>
Les membres du personnel concernés par l’approvisionnement ou la livraison et donc de ce fait en contact 
soit avec les fournisseurs, soit avec les clients, doivent prendre connaissance et tenir compte des 
dispositions prévues dans les différents protocoles de sécurité signés tant en amont qu’en aval. <br>

<b>ARTICLE 4 – ECHELLE ET NATURE DES SANCTIONS DISCIPLINAIRES</b><br>
Constitue une sanction, toute mesure autre que les observations verbales, prise par l’entreprise, à la suite 
d’un agissement d’un salarié considéré comme fautif. <br>
Les sanctions susceptibles d’être prises suivant le degré de gravité de la faute sont les suivantes : <br>
-  Avertissement écrit ; <br>
-  Mise à pied de 1 jour à une semaine ; <br>
-  Mutation disciplinaire ; <br>
-  Licenciement pour faute grave . <br>
-  Licenciement pour faute lourde.<br>

<b>ARTICLE 5 – DROITS DE LA DEFENSE DES SALARIES </b>
Procédure disciplinaire : <br>
Aucune faute ne peut être invoquée au-delà d’un délai de 2 mois à compter du jour où l’entreprise en a eu 
connaissance, à moins que des poursuites pénales n’aient été exercées dans ce même délai. <br>
Aucune sanction antérieure de plus de 3 trois ans à l’engagement des poursuites disciplinaires ne peut 
être invoquée à l’appui d’une nouvelle sanction. <br>
Toute sanction disciplinaire notifiée doit comporter l’énonciation des griefs qui la motive. <br>
Toute sanction disciplinaire est précédée d’une convocation du salarié. Le salarié peut se faire assister 
d’un autre salarié de l’entreprise lors de l’entretien. La sanction ne peut intervenir moins d’un jour franc, ni plus d’un mois après le jour fixé pour l’entretien <br>
<b>ALCOOTEST </b><br>
Pour des emplois dont il convient de déterminer la liste, il peut être fait recours à l’alcootest. Il faut que ces emplois comportent des risques pour les biens ou les personnes en cas de consommation excessive 
d’alcool. <br>
L’entreprise peut mettre à la disposition des salariés concernés des alcootests à titre préventif. <br>

Elle peut, également, procéder devant témoin, à un contrôle d’alcootest à condition d’avertir le salarié 
concerné de la possibilité qui lui est faite de bénéficier d’une contre-expertise. <br>
<b>VOLS DANS L’ENTREPRISE</b><br>
En cas de disparition renouvelée et rapprochée de matériels, marchandises ou objets appartenant à 
l’entreprise, celle-ci se réserve la possibilité de demander au Personnel de se soumettre à toute mesure 
de vérification des objets transportés à l’exclusion de toute fouille corporelle. <br>
Les salariés concernés peuvent demander l’assistance d’un représentant du Personnel et pourront en tout 
état de cause refuser une telle vérification.<br>


<b>ARTICLE 6 – RESPECT DE LA DIGNITE DU SALARIE</b><br>
<b>HARCELEMENT SEXUEL</b> <br>
Aucun salarié ne peut faire l’objet d’une sanction pour avoir subi ou refusé les agissements de 
harcèlement d’une personne qui, abusant de l’autorité que lui confèrent ses fonctions, a donné des ordres, 
proféré des menaces, et imposé des contraintes ou exercé des pressions de toute nature sur ce salarié, 
dans le but d’obtenir des faveurs de nature sexuelle à son profit ou au profit d’un tiers. 
En revanche, est passible d’une sanction disciplinaire, toute personne qui, dans l’exercice de ses 
fonctions, aura procédé à de tels agissements. 
<b>HARCELEMENT MORAL</b><br>
Aucun salarié ne doit subir des agissements répétés de harcèlement moral qui ont pour objet ou pour effet 
une dégradation des conditions de travail susceptibles de porter atteinte à ses droits et à sa dignité, 
d’altérer sa santé physique ou de compromettre son avenir professionnel. Aucun salarié ne peut être 
sanctionné, licencié ou faire l’objet d’une mesure discriminatoire pour avoir subi ou refusé de subir les 
agissements définis ci-dessus ou pour avoir témoigné de tels agissements ou les avoir relatés. <br>
En, revanche, est passible d’une sanction disciplinaire tout salarié qui aura procédé à de tels agissements. 
